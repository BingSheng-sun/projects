# ===================== 3. 辅助函数 =====================
def standardize_province_name(df):
    if "省份" not in df.columns:
        return df
    df["省份"] = df["省份"].replace(PROVINCE_MAPPING)
    return df

def read_data_file(file_path):
    file_name = os.path.basename(file_path)
    if file_path.endswith(".xlsx"):
        df = pd.read_excel(file_path, engine="openpyxl")
        df.rename(columns={
            "year": "年份", "prov_name": "省份", "index_aggregate": "普惠金融总指数",
            "coverage_breadth": "普惠金融覆盖广度", "usage_depth": "普惠金融使用深度",
            "digitization_level": "普惠金融数字化程度"
        }, inplace=True)
        if "省份" in df.columns:
            df = df[df["省份"].isin(VALID_PROVINCES)]
        print(f"✅ 读取Excel：{file_name}，形状：{df.shape}")
        return standardize_province_name(df)
    elif file_path.endswith(".csv"):
        try:
            df = pd.read_csv(file_path, encoding="utf-8-sig")
        except:
            df = pd.read_csv(file_path, encoding="gbk")
        df.columns = df.columns.str.replace("\ufeff", "").str.strip()
        print(f"✅ 读取CSV：{file_name}，形状：{df.shape}")
        return standardize_province_name(df)
    else:
        raise ValueError(f"不支持的文件格式：{file_name}")

