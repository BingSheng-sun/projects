df_merge = df_merge.sort_values(["省份", "年份"]).reset_index(drop=True)
df_raw = df_merge.copy()
print(f"✅ 合并完成！总样本量：{df_merge.shape[0]}，总指标数：{df_merge.shape[1]}")

# ===================== 6. 缺失值处理 =====================
print("\n===================== 开始缺失值处理 =====================")
numeric_cols = df_merge.select_dtypes(include=[np.number]).columns.tolist()
numeric_cols = [col for col in numeric_cols if col != "年份"]

for province in df_merge["省份"].unique():
    mask = df_merge["省份"] == province
    df_merge.loc[mask, numeric_cols] = df_merge.loc[mask, numeric_cols].interpolate(
        method="linear", limit_direction="both", axis=0
    )
df_merge[numeric_cols] = df_merge[numeric_cols].fillna(df_merge[numeric_cols].mean())
print(f"✅ 缺失值处理完成！")

# ===================== 7. 价格平减 =====================
print("\n===================== 开始价格平减（2011年基期）=====================")
# 自动识别价值型指标（名称中包含“亿元”、“产值”、“支出”、“GDP”、“增加值”的列）
value_cols = [col for col in numeric_cols if any(keyword in str(col) for keyword in ["亿元", "产值", "支出", "GDP", "增加值", "生产总值"])]
if not value_cols:
    value_cols = numeric_cols[:5] # 如果没识别到，默认取前5列
print(f"💰 识别到需平减指标：{value_cols}")

# 计算定基指数
df_deflator = df_deflator.sort_values(["省份", "年份"]).reset_index(drop=True)
df_deflator["定基平减指数(2011=100)"] = np.nan
for province in df_deflator["省份"].unique():
    mask = df_deflator["省份"] == province
    df_deflator.loc[mask & (df_deflator["年份"] == 2011), "定基平减指数(2011=100)"] = 100
    for year in range(2012, 2024):
        year_mask = mask & (df_deflator["年份"] == year)
        last_year_mask = mask & (df_deflator["年份"] == year - 1)
        if not df_deflator.loc[last_year_mask].empty and not df_deflator.loc[year_mask].empty:
            df_deflator.loc[year_mask, "定基平减指数(2011=100)"] = (
                df_deflator.loc[last_year_mask, "定基平减指数(2011=100)"].iloc[0]
                * df_deflator.loc[year_mask, "GDP平减指数"].iloc[0]
                / 100
            )

df_merge = pd.merge(df_merge, df_deflator[["省份", "年份", "定基平减指数(2011=100)"]], on=["省份", "年份"], how="left")
new_constant_cols = []
for col in value_cols:
    if col not in df_merge.columns:
        continue
    constant_col = f"{col}_2011年不变价"
    df_merge[constant_col] = df_merge[col] / (df_merge["定基平减指数(2011=100)"] / 100)
    new_constant_cols.append(constant_col)
    print(f"✅ 完成平减：{col}")
numeric_cols.extend(new_constant_cols)

# ===================== 8. 无量纲化 =====================
print("\n===================== 开始无量纲化处理 =====================")
scale_cols = numeric_cols
for col in scale_cols:
    if col not in df_merge.columns: continue
    z_col = f"{col}_Zscore标准化"
    mean_val, std_val = df_merge[col].mean(), df_merge[col].std()
    if std_val != 0: df_merge[z_col] = (df_merge[col] - mean_val) / std_val

# ===================== 9. 生成统计对比表并保存 =====================
print("\n===================== 正在保存结果文件 =====================")
def get_stats_df(df, cols, data_type):
    stats_list = []
    for col in cols:
        if col not in df.columns: continue
        data = df[col]
        stats_list.append({
            "指标名": col, "数据类型": data_type, "样本量": len(data.dropna()),
            "均值": round(data.mean(), 4), "标准差": round(data.std(), 4),
            "最小值": round(data.min(), 4), "最大值": round(data.max(), 4),
            "缺失值数量": data.isnull().sum()
        })
    return pd.DataFrame(stats_list)

df_stats_raw = get_stats_df(df_raw, numeric_cols, "预处理前")
df_stats_clean = get_stats_df(df_merge, numeric_cols, "预处理后")
df_compare = pd.concat([df_stats_raw, df_stats_clean], axis=0)

# 保存Excel统计对比表
compare_excel_path = os.path.join(output_folder, "面板数据预处理前后统计对比表.xlsx")
with pd.ExcelWriter(compare_excel_path, engine="openpyxl") as writer:
    df_compare.to_excel(writer, sheet_name="前后总对比", index=False)
    df_stats_raw.to_excel(writer, sheet_name="预处理前", index=False)
    df_stats_clean.to_excel(writer, sheet_name="预处理后", index=False)

# 保存CSV数据
full_data_path = os.path.join(output_folder, "农业面板数据_完整预处理版.csv")
df_merge.to_csv(full_data_path, index=False, encoding="utf-8-sig")

model_cols = ["省份", "年份"] + [col for col in df_merge.columns if "不变价" in col or "Zscore" in col or "普惠金融" in col]
df_model = df_merge[model_cols].copy()
